const image = document.getElementById('image-color');
function getRandomColor() {
    const letters = '0123456789ABCDEF';
    let color = '#';
    for (let i = 0; i < 6; i++) {
      color += letters[Math.floor(Math.random() * 16)];
    }
    return color;
  }

  image.addEventListener('click', () => {
    document.body.style.backgroundColor = getRandomColor();
  });



    

  function displayDateAndDay() {
    const nowDay = new Date();
    
    
    const day = nowDay.toLocaleString('en-US', { weekday: 'long' }); 
    const date = nowDay.getDate(); 
    const month = nowDay.toLocaleString('en-US', { month: 'long' }); 
    const year = nowDay.getFullYear(); 

    
    const formattedDate = `${day}, ${month} ${date}, ${year}`;

    document.getElementById('dateDay').innerText = formattedDate;
}


